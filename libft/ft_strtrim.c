#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*final;
	size_t	start;
	size_t	end;

	if (s1 == NULL || set == NULL)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	final = (char *)malloc((end - start + 1) * sizeof(char));
	if (final == NULL)
		return (NULL);
	ft_strlcpy(final, &s1[start], end - start + 1);
	return (final);
}
